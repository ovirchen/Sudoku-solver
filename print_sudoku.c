#include "sudoku.h"

void	print_sudoku(int **sud)
{
	int		i;
	int		j;
	char	c;

	j = 1;
	while (j <= 9)
	{
		i = 1;
		while (i <= 9)
		{
			c = sud[j][i] + '0';
			write(1, &c, 1);
			if (i != 9)
				write(1, " ", 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
}
